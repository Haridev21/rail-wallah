const Map<String, String> kNameKeywordToTemplate = {
  'rajdhani': 'rajdhani',
  'shatabdi': 'shatabdi',
  'jan shatabdi': 'shatabdi',
  'duronto': 'duronto',
  'vande bharat': 'shatabdi',
  'tejas': 'shatabdi',
  'gatimaan': 'shatabdi',
};




List<CoachConfig> getCoachTemplate(String trainNo, String trainName) {
  final template = kTrainNumberToTemplate[trainNo.trim()];
  if (template != null) return _fromTemplateName(template);

  final nameLower = trainName.toLowerCase();
  for (final entry in kNameKeywordToTemplate.entries) {
    if (nameLower.contains(entry.key)) return _fromTemplateName(entry.value);
  }

  return _defaultConsist();
}

List<CoachConfig> _fromTemplateName(String name) {
  switch (name) {
    case 'rajdhani':
      return _rajdhaniConsist();
    case 'shatabdi':
      return _shatabdiConsist();
    case 'duronto':
      return _durontoConsist();
    case 'superfast':
      return _superfastConsist();
    default:
      return _defaultConsist();
  }
}
